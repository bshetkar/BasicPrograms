package packageA;

public class SwapProgramUsingThirdVaraiable {
	public static void main(String[] args) {
		int a=30;
		int b=40;
		int temp;
		System.out.println("Before the swapping result of a:" +a);
		System.out.println("Before the swapping result of b:" +b);
		a=b;
		temp=b;
		b=temp;
		System.out.println("After the swapping result of a:" +a);
		System.out.println("Before the swapping result of b:" +b);
		
	}
	

}
